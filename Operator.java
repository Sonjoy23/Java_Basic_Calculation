public class Operator {
    public static void main(String[] args) {
        // Addition
        int a = 30;
        int b = 50;
        System.out.println("total number is: "+(a+b));
        // Subtraction
        int c = 50;
        int d = 25;
        System.out.println("Subtraction number is: "+(c-d));
        // Multiplication
        int e = 5;
        int f = 6;
        System.out.println("multiple number is: "+(e*f));
        // Division
        int g = 50;
        int h = 5;
        System.out.println("division number is: "+(g/h));
        // Modulus
        int i = 22;
        int j = 10;
        System.out.println("modulus number is:"+(i%j));

    }
}